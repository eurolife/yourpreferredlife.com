import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const API_BASE_URL = "https://api.lemonsqueezy.com/v1";
const outputPath = resolve("src/lib/lemonProducts.generated.ts");
let cachedEnvFile;

function readEnvFile() {
  if (cachedEnvFile) {
    return cachedEnvFile;
  }

  const envPath = resolve(".env");

  if (!existsSync(envPath)) {
    cachedEnvFile = {};
    return cachedEnvFile;
  }

  cachedEnvFile = Object.fromEntries(
    readFileSync(envPath, "utf8")
      .split(/\r?\n/)
      .map((line) => line.trim())
      .filter((line) => line && !line.startsWith("#") && line.includes("="))
      .map((line) => {
        const separatorIndex = line.indexOf("=");
        const key = line.slice(0, separatorIndex).trim();
        const value = line
          .slice(separatorIndex + 1)
          .trim()
          .replace(/^['"]|['"]$/g, "");

        return [key, value];
      }),
  );

  return cachedEnvFile;
}

function getEnv(name) {
  return process.env[name] ?? readEnvFile()[name];
}

function slugify(value) {
  return value
    .toLowerCase()
    .replace(/&/g, " and ")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function uniqueSlug(baseSlug, usedSlugs) {
  let slug = baseSlug;
  let index = 2;

  while (usedSlugs.has(slug)) {
    slug = `${baseSlug}-${index}`;
    index += 1;
  }

  usedSlugs.add(slug);
  return slug;
}

async function lemonRequest(path) {
  const apiKey = getEnv("LEMON_SQUEEZY_API_KEY");

  if (!apiKey) {
    throw new Error("Missing LEMON_SQUEEZY_API_KEY. Add it to .env or your shell environment.");
  }

  const response = await fetch(`${API_BASE_URL}${path}`, {
    headers: {
      Accept: "application/vnd.api+json",
      "Content-Type": "application/vnd.api+json",
      Authorization: `Bearer ${apiKey}`,
    },
  });

  if (!response.ok) {
    throw new Error(`Lemon Squeezy API error ${response.status}: ${await response.text()}`);
  }

  return response.json();
}

async function listAll(path, searchParams = {}) {
  const resources = [];
  let page = 1;
  let hasNextPage = true;

  while (hasNextPage) {
    const params = new URLSearchParams({
      "page[size]": "100",
      "page[number]": String(page),
      ...searchParams,
    });
    const result = await lemonRequest(`${path}?${params.toString()}`);

    resources.push(...result.data);
    hasNextPage = Boolean(result.links?.next) || resources.length < (result.meta?.page?.total ?? 0);
    page += 1;
  }

  return resources;
}

function getProductPriceCents(product, variant) {
  return Number(variant?.attributes?.price ?? product.attributes.price ?? 0);
}

function getProductDescription(product, variant) {
  return (
    variant?.attributes?.description ||
    product.attributes.description ||
    ""
  ).replace(/\s+/g, " ").trim();
}

function makeCatalogProduct(product, variant, usedSlugs) {
  const productSlug = product.attributes.slug || slugify(product.attributes.name);
  const variantSlug = variant?.attributes?.slug;
  const baseSlug = variantSlug && variantSlug !== "default"
    ? `${productSlug}-${variantSlug}`
    : productSlug;

  return {
    slug: uniqueSlug(baseSlug, usedSlugs),
    title: variant?.attributes?.name && variant.attributes.name !== "Default"
      ? `${product.attributes.name} - ${variant.attributes.name}`
      : product.attributes.name,
    description: getProductDescription(product, variant),
    priceCents: getProductPriceCents(product, variant),
    currency: "GBP",
    lemonSqueezy: {
      productId: product.id,
      variantId: variant?.id ?? "",
      buyNowUrl: product.attributes.buy_now_url || undefined,
      productStatus: product.attributes.status,
      variantStatus: variant?.attributes?.status,
      testMode: Boolean(product.attributes.test_mode || variant?.attributes?.test_mode),
    },
  };
}

function toGeneratedFile(catalogProducts) {
  const serializedProducts = JSON.stringify(catalogProducts, null, 2)
    .replace(/"([^"]+)":/g, "$1:");

  return `import type { LemonCatalogProduct } from "./products";

// Generated by \`pnpm sync:products\`.
// Do not edit this file directly. Edit Lemon Squeezy product data or \`productOverrides.ts\`.
export const lemonCatalogProducts = ${serializedProducts} satisfies LemonCatalogProduct[];
`;
}

async function main() {
  const storeId = getEnv("LEMON_SQUEEZY_STORE_ID");
  const productFilters = storeId ? { "filter[store_id]": storeId } : {};
  const products = await listAll("/products", productFilters);
  const variants = await listAll("/variants");
  const variantsByProductId = variants.reduce((groupedVariants, variant) => {
    const productId = String(variant.attributes.product_id);
    const productVariants = groupedVariants.get(productId) ?? [];

    productVariants.push(variant);
    groupedVariants.set(productId, productVariants);

    return groupedVariants;
  }, new Map());
  const usedSlugs = new Set();
  const catalogProducts = products.flatMap((product) => {
    const productVariants = variantsByProductId.get(product.id) ?? [];

    if (productVariants.length === 0) {
      return [makeCatalogProduct(product, undefined, usedSlugs)];
    }

    return productVariants.map((variant) => makeCatalogProduct(product, variant, usedSlugs));
  });

  writeFileSync(outputPath, toGeneratedFile(catalogProducts));
  console.log(`Synced ${catalogProducts.length} product entries to ${outputPath}.`);
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exitCode = 1;
});
